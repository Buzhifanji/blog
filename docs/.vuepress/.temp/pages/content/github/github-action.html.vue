<h2 id="github-action-是什么" tabindex="-1"><a class="header-anchor" href="#github-action-是什么" aria-hidden="true">#</a> Github Action 是什么</h2>
<p>我们的项目会从构建、测试、打包、发布或者部署四个过程，GitHub把这一系列操作称为Action。</p>
<p>每当添加新的功能的时候，需要重新经历上述四个过程，我们可以手动进行这些操作，但这样会很繁琐，如果有脚本可以让这一系列操作自动执行可以大大提高我</p>
<p>们的工作效率。GitHub Action就是做这样的事情，它会帮助我们自动执行周期性的开发任务。需要注意的是，<strong>GitHub Actions 是事件驱动的</strong>，也就是意味着我们</p>
<p>可以指定事件来触发一系列指令执行，例如，当我们提交代码的时候，可以触发自动执行测试代码。</p>
